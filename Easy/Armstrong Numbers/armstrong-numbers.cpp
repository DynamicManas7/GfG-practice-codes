class Solution {
  public:
    string armstrongNumber(int n) {
        // code here
        int b = 0;
        int temp = n;
        while(n>0){
           int a = n%10;
            b += (a*a*a);
           n = n / 10;
        }
        if(temp == b){
            return "true";
        }else{
            return "false";
        }
    }
};

